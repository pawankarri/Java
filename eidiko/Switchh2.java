package eidiko;

public class Switchh2 {
	public static void main(String[] args) {
		int num=10;
		switch(num)
		{
		case 1:
			System.out.println("tne number is:"+num);
			break;
		case 2:
			System.out.println("tne number is:"+num);
			break;
		case 3:
			System.out.println("tne number is:"+num);
			break;
			default:
				System.out.println("tne number is:"+num);
		}
	}

}
